package br.com.lojavirtual.instrumento;

public class Flauta {
	private double Preço;
	private boolean Transversa;
	
	public double getPreço() {
		return Preço;
	}
	public void setPreço(double preço) {
		Preço = preço;
	}
	public boolean isTransversa() {
		return Transversa;
	}
	public void setTransversa(boolean transversa) {
		this.Transversa = transversa;
	}

	
}
