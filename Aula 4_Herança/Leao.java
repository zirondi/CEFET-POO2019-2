
public class Leao {
	private String nome;
	private int idade;
	private double peso;
	private String cor;
	
	public void setNome(String n) {
		nome = n;
	}
	public String getNome() {
		return nome;
	}
	
	public void setIdadde(int i) {
		idade = i;
	}
	public int getIdade() {
		return idade;
	}
	
	public void setPeso(double p) {
		peso = p;
	}
	public double getPeso() {
		return peso;
	}
	
	public void setCor(String c) {
		cor = c;
	}
	public String getCor() {
		return cor;
	}
}
